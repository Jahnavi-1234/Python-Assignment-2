# Define a dictionary with string keys and integer values
dict = {"Gfg": 1, "is": 2, "Best": 3}
# Create a list of the keys from the dictionary
dict1 = list(dict.keys())
# Create a list of the values from the dictionary
dict2 = list(dict.values())
# Concatenate the lists of keys and values
result = dict1 + dict2
print(result)
